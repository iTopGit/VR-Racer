using System.Collections;
using System.Collections.Generic;
using UnityEngine;


public class TextMeshProLoop : MonoBehaviour
{
    [SerializeField] private float speed = 100f;
    [SerializeField] private float loopDistance = 10f;

    private RectTransform textRectTransform;
    private Vector2 initialPosition;

    void Start()
    {
        textRectTransform = GetComponent<RectTransform>();
        initialPosition = textRectTransform.anchoredPosition;
    }
    void Update()
    {

        // ?????????????????????????????????????????????
        textRectTransform.anchoredPosition += Vector2.left * speed * Time.deltaTime;

        // ??????????????????????????????????????
        if (textRectTransform.anchoredPosition.x < -(initialPosition.x + loopDistance))
        {
            // ?????????????????????????????????????????????
            textRectTransform.anchoredPosition = initialPosition;
        }
    }

}
